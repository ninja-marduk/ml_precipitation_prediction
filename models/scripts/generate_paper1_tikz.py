SCRIPT_DIR = Path(__file__).resolve().parent
PROJECT_ROOT = SCRIPT_DIR.parents[1]
TIKZ_DIR = PROJECT_ROOT / "docs" / "papers" / "1" / "latex" / "tikz"

# --- Tree layout constants (cm) ---
ROOT_X = 2.0       # root node center x
MID_X = 6.2        # mid node center x
LEAF_X = 12.5      # leaf node center x
LEAF_VSPACE = 0.58  # vertical spacing between leaves
GROUP_GAP = 0.58    # must equal LEAF_VSPACE to prevent overlap (nodes have min height)


# =========================================================================
# UTILITY FUNCTIONS
# =========================================================================

def _escape_tex(text):
    """Escape special LaTeX characters in node text."""
    text = text.replace("&", "\\&")
    text = text.replace("%", "\\%")
    text = text.replace("#", "\\#")
    # Don't escape _ or $ as they may be intentional
    return text


def _multiline(text):
    """Convert Python newlines to TikZ \\\\ for multiline nodes."""
    return text.replace("\n", "\\\\")


# =========================================================================
# TREE DIAGRAM GENERATOR
# =========================================================================

def generate_tree(root_label, branches, filename):
    """Generate a TikZ horizontal tree diagram.

    Layout: Root (left) → Mid nodes (center) → Leaf nodes (right).
    Connections are straight lines from .east anchors to .west anchors.
    """
    branch_names = list(branches.keys())
    branch_leaves = list(branches.values())
    n_branches = len(branch_names)

    # --- Compute vertical positions ---
    # y decreases from top (0) downward (negative values)
    y_cursor = 0.0
    positions = []  # list of (mid_y, [leaf_ys]) per branch

    for i, leaves in enumerate(branch_leaves):
        n = len(leaves)
        leaf_ys = [y_cursor - j * LEAF_VSPACE for j in range(n)]
        mid_y = (leaf_ys[0] + leaf_ys[-1]) / 2.0 if n > 0 else y_cursor
        positions.append((mid_y, leaf_ys))
        if n > 0:
            y_cursor = leaf_ys[-1] - GROUP_GAP
        else:
            y_cursor -= GROUP_GAP

    # Root y = centroid of all mid-node positions
    mid_ys_all = [p[0] for p in positions]
    root_y = sum(mid_ys_all) / len(mid_ys_all)

    # --- Build TikZ code ---
    L = []
    L.append("% Auto-generated by generate_paper1_tikz.py")
    L.append("\\begin{tikzpicture}")
    L.append("")

    # Root node
    root_text = _multiline(_escape_tex(root_label))
    L.append(f"  \\node[rootnode] (root) at ({ROOT_X:.2f},{root_y:.2f}) {{{root_text}}};")
    L.append("")

    # Mid and leaf nodes
    for i, (name, leaves) in enumerate(zip(branch_names, branch_leaves)):
        mid_y, leaf_ys = positions[i]
        mid_id = f"m{i}"
        mid_text = _multiline(_escape_tex(name))

        clean_name = name.replace("\n", " ")
        L.append(f"  % Branch {i}: {clean_name}")
        L.append(f"  \\node[midnode] ({mid_id}) at ({MID_X:.2f},{mid_y:.2f}) {{{mid_text}}};")

        for j, (leaf_text, ly) in enumerate(zip(leaves, leaf_ys)):
            leaf_id = f"l{i}{j}"
            L.append(f"  \\node[leafnode] ({leaf_id}) at ({LEAF_X:.2f},{ly:.2f}) {{{_escape_tex(leaf_text)}}};")
        L.append("")

    # Connections: Root → Mid
    L.append("  % Root to Mid connections")
    for i in range(n_branches):
        L.append(f"  \\draw[conn] (root.east) -- (m{i}.west);")
    L.append("")

    # Connections: Mid → Leaf
    L.append("  % Mid to Leaf connections")
    for i, leaves in enumerate(branch_leaves):
        for j in range(len(leaves)):
            L.append(f"  \\draw[conn] (m{i}.east) -- (l{i}{j}.west);")
    L.append("")

    L.append("\\end{tikzpicture}")

    # Write file
    output = TIKZ_DIR / filename
    output.write_text("\n".join(L), encoding="utf-8")
    total_leaves = sum(len(v) for v in branches.values())
    print(f"  {filename}: {n_branches} branches, {total_leaves} leaves, {len(L)} lines")


