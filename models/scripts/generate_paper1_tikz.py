# PRISMA FLOW DIAGRAM GENERATOR
# =========================================================================

def generate_prisma(filename):
    """Generate a TikZ PRISMA 2020 flow diagram.

    Layout: 5 rows × 2 columns with vertical and horizontal arrows.
    Stage labels rotated 90° on the left margin.
    """
    # Row y positions (top to bottom, 2.8cm spacing)
    RY = [11.2, 8.4, 5.6, 2.8, 0.0]
    LX = 3.8    # left column center x
    RX = 11.8   # right column center x

    L = []
    L.append("% Auto-generated by generate_paper1_tikz.py")
    L.append("\\begin{tikzpicture}")
    L.append("")

    # Stage labels (rotated, left margin)
    L.append("  % Stage labels")
    stages = [
        (RY[0], "IDENTIFICATION"),
        (RY[1], "SCREENING"),
        ((RY[2] + RY[3]) / 2, "ELIGIBILITY"),
        (RY[4], "INCLUDED"),
    ]
    for y, label in stages:
        L.append(f"  \\node[rotate=90, font=\\sffamily\\bfseries\\scriptsize, text=iwablue]"
                 f" at (0.3,{y:.2f}) {{{label}}};")
    L.append("")

    # Row 1: Identification
    L.append("  % IDENTIFICATION")
    L.append(f"  \\node[prismabox] (id1) at ({LX},{RY[0]:.1f})"
             f" {{Records identified through\\\\database searching\\\\(n~=~452)}};")
    L.append(f"  \\node[prismalt] (id2) at ({RX},{RY[0]:.1f})"
             f" {{Additional records via\\\\snowball technique\\\\(n~=~0)}};")
    L.append("")

    # Row 2: Screening
    L.append("  % SCREENING")
    L.append(f"  \\node[prismabox] (sc1) at ({LX},{RY[1]:.1f})"
             f" {{Records after duplicates\\\\removed\\\\(n~=~322)}};")
    L.append(f"  \\node[prismaexcl] (sc2) at ({RX},{RY[1]:.1f})"
             f" {{Duplicates removed\\\\(n~=~130)}};")
    L.append("")

    # Row 3: Eligibility - screened
    L.append("  % ELIGIBILITY (screened)")
    L.append(f"  \\node[prismabox] (el1) at ({LX},{RY[2]:.1f})"
             f" {{Records screened\\\\(n~=~322)}};")
    L.append(f"  \\node[prismaexcl] (el2) at ({RX},{RY[2]:.1f})"
             f" {{Records excluded\\\\(title/abstract review)\\\\(n~=~160)}};")
    L.append("")

    # Row 4: Eligibility - full text
    L.append("  % ELIGIBILITY (full text)")
    L.append(f"  \\node[prismabox] (el3) at ({LX},{RY[3]:.1f})"
             f" {{Full-text articles assessed\\\\for eligibility\\\\(n~=~162)}};")
    L.append(f"  \\node[prismaexcl] (el4) at ({RX},{RY[3]:.1f})"
             f" {{Full-text articles excluded\\\\(n~=~77)}};")
    L.append("")

    # Row 5: Included
    L.append("  % INCLUDED")
    L.append(f"  \\node[prismabox] (in1) at ({LX},{RY[4]:.1f})"
             f" {{Studies included in\\\\final analysis\\\\(n~=~85)}};")
    L.append(f"  \\node[prismalt] (in2) at ({RX},{RY[4]:.1f})"
             f" {{Studies included in\\\\quantitative synthesis\\\\(n~=~34)}};")
    L.append("")

    # Vertical arrows (main flow, left column)
    L.append("  % Vertical arrows (main flow)")
    flow_pairs = [("id1", "sc1"), ("sc1", "el1"), ("el1", "el3"), ("el3", "in1")]
    for top, bot in flow_pairs:
        L.append(f"  \\draw[-{{Latex[length=2.5mm]}}, line width=1.2pt, black!50]"
                 f" ({top}.south) -- ({bot}.north);")
    L.append("")

    # Horizontal arrows (to exclusion/secondary boxes)
    L.append("  % Horizontal arrows")
    h_pairs = [("sc1", "sc2"), ("el1", "el2"), ("el3", "el4"), ("in1", "in2")]
    for left, right in h_pairs:
        L.append(f"  \\draw[-{{Latex[length=2.5mm]}}, line width=1.2pt, black!50]"
                 f" ({left}.east) -- ({right}.west);")
    L.append("")

    L.append("\\end{tikzpicture}")

    # Write file
    output = TIKZ_DIR / filename
    output.write_text("\n".join(L), encoding="utf-8")
    print(f"  {filename}: PRISMA flow diagram, {len(L)} lines")


