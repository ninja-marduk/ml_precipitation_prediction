"""
Q1 Journal Figure Standards - Shared Configuration
===================================================
Based on Nature, PNAS, AGU, Elsevier, and MDPI guidelines.
Uses Okabe-Ito colorblind-safe palette (Nature-recommended).

GOLD STANDARD REFERENCE: spatial_r2_map_3panel (Figure 9 of Paper 5,
generated by generate_poster_figures.py::poster_spatial_r2_3panel).
The font hierarchy below is calibrated so that, when an image of width
~20 inches is included in a paper at 0.98\\textwidth (~7 inches), the
on-page rendered sizes are:
    - subplot title  ~  7.0 pt
    - axis labels    ~  6.3 pt
    - tick labels    ~  4.9 pt
    - panel label    ~  7.0 pt
which is the visual reference Octavio approved (2026-04-30).

USAGE
-----
    from figure_config import COLORS, setup_paper_style
    setup_paper_style()           # for paper / thesis figures (single rcParams)

    from figure_config import setup_poster_style
    setup_poster_style()          # for poster figures (~1m viewing distance)

    add_panel_label(ax, 'a')      # consistent (a)/(b)/(c) panel labels


PROFILES
--------
PAPER_RC: target on-page sizes 7-8 pt at typical Elsevier / MDPI / IWA
          textwidth. Use for any figure included in paper.tex / thesis.tex
          via \\includegraphics{...}.

POSTER_RC: native sizes 14-20 pt for A0 / A1 prints viewed from ~1 m.
           Use for poster generation only. When the same image is reused
           inside a paper at 0.98\\textwidth the fonts scale down to the
           PAPER_RC target.


EXCEPTIONS POLICY
-----------------
Any figure whose generator deviates from these profiles must declare the
deviation as a one-line docstring comment near the relevant function:

    # FIGURE EXCEPTION: <reason>, e.g. "graphical abstract requires
    # larger title for visibility at thumbnail size"
"""

