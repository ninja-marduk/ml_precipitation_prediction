# ---------------------------------------------------------------------------
# Style setters
# ---------------------------------------------------------------------------
def setup_paper_style():
    """Apply paper / thesis figure rcParams (Q1 journal target)."""
    plt.style.use('seaborn-v0_8-whitegrid')
    plt.rcParams.update(PAPER_RC)


def setup_poster_style():
    """Apply poster rcParams (gold standard from spatial_r2_map_3panel)."""
    plt.style.use('seaborn-v0_8-whitegrid')
    plt.rcParams.update(POSTER_RC)


# Backwards-compatible alias (existing scripts import setup_style)
def setup_style():
    """Legacy alias for setup_paper_style(). Prefer setup_paper_style going forward."""
    setup_paper_style()


# ---------------------------------------------------------------------------
# Panel labels (a)/(b)/(c)
# ---------------------------------------------------------------------------
def add_panel_label(ax, label, x=-0.08, y=1.05, fontsize=None):
    """Add bold panel label (a), (b), etc. to top-left of axis.

    For paper figures the default uses 9 pt; for poster figures pass
    fontsize=20 to match the gold standard.
    """
    if fontsize is None:
        fontsize = plt.rcParams['axes.titlesize']
    ax.text(x, y, f'({label})', transform=ax.transAxes,
            fontsize=fontsize, fontweight='bold', va='top', ha='right')


